
public class Nelilaskin {

    public static void main(String[] args) {
        int eka = 5;
        int toka = 3;

        System.out.println(eka + " + " + toka + " = " + (eka + toka));

        // Toteuta loput nelilaskimesta tähän.
        
        System.out.println(eka + " - " + toka + " = " + (eka - toka));
        System.out.println(eka + " * " + toka + " = " + (eka * toka));
        System.out.println(eka + " / " + toka + " = " + (1.0 * eka / toka));
    }
}
